function trocarimagem13() {
    const imgElement = document.getElementById('imagem13');
    const imgElement2 = document.getElementById('imagem'); 
    const cardElement = imgElement.closest('.card');
    const currentSrc = imgElement.getAttribute('src');

    if (currentSrc === '../imgs/Broken Heart2.png') {
        imgElement.setAttribute('src', '../imgs/Broken Heart.png');
        cardElement.classList.add('vermelho-outline');
        cardElement.classList.remove('verdin-outline');
        imgElement2.classList.add('desativar'); 
    } else {
        imgElement.setAttribute('src', '../imgs/Broken Heart2.png');
        cardElement.classList.remove('vermelho-outline');
        cardElement.classList.remove('verdin-outline');
        imgElement2.classList.remove('desativar'); 
    }
}

